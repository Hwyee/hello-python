# 比较运算符
# 判断不相等
a = 10
b = 20
if a != b:
    print("a != b")
# 判断相等
if a == b:
    print("a == b")

# 判断大于
if a > b:
    print("a > b")
# 判断小于
if a < b:
    print("a < b")
# 判断大于等于
if a >= b:
    print("a >= b")
# 判断小于等于
if a <= b:
    print("a <= b")

# 字符串的比较 比较的是字符串的ascii码
s1 = "abc"
s2 = "abc"
if s1 == s2:
    print("s1 == s2")
if s1 != s2:
    print("s1 != s2")
if s1 > s2:
    print("s1 > s2")
if s1 < s2:
    print("s1 < s2")
if s1 >= s2:
    print("s1 >= s2")
